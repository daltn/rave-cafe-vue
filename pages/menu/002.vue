      <h3>The Positive-Sum Manifesto</h3>
      <p class="center">by n3wb</p>
      <p>
        There are countless interactions and transactions we make with the
        modern world around us each day that involve an inherent tension between
        our wants and needs, and those of others. Underneath each of these
        moments are social contracts we willingly accept, sometimes begrudgingly
        when they are exploitative in nature. Our technology, our food, our
        relationships, and our work are just a few examples of domains where we
        make daily negotiations.
      </p>
      <p>
        Humans have slowly evolved over hundreds of thousands of years, while
        new technologies are introduced and innovated upon every day. While each
        aspect of our modern lives can cause us harm if we stay in autopilot, we
        col- lectively have the power to make positive-sum decisions that
        benefit both ourselves and the world around us.
      </p>
      <p>
        Within the broader principles of game theory are zero-sum games whereby,
        as the dictionary defines it, “positive sum occurs when resources are
        somehow increased and an approach is formulated in which the desires and
        needs of all concerned are satisfied.” Whereas zero-sum games involve
        one party benefiting from the other party's loss, positive-sum games
        revolve around the collective good of both players.
      </p>
      <p>
        The pandemic woke us up to the realities of the systems around us that
        create zero-sum dynamics. Our modern healthcare system is optimized
        around a “fee for service” model that is divorced from health outcomes.
        Our agricultural and food systems are designed around cheap abundance
        that ultimately makes us sicker. Our social networks and mobile devices
        are designed to keep us continuously plugged-in, which ultimately has
        led to an increase in loneliness and anxiety.
      </p>
      <p>
        If we wait for these systems to redesign themselves as positive-sum
        regenerative fly- wheels, we'll quickly deplete ourselves and the
        resources around us. Instead, we must remain constantly alert and
        vigilant, engaging only in activities that nourish our bodies and souls,
        leaving us better off than we were yesterday. The second-order effects
        of everything must be considered from the tap of a phone to the meat on
        our plate. It may feel like a fight at first, but once we all experience
        the magic of positive-sum games, we'll keep playing them indefinitely.
      </p>
