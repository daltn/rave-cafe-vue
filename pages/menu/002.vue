      <h3>Food for Thought</h3>
      <p class="center">by Dalton</p>
      <p>
        Building a relationship to our food is essential to unlocking our full
        potential as humans. Our food choices are the main factor in how we feel
        each day. Being intentional with our food choices is an evolving process
        that has various levels. Do your own research and experiment to figure
        out what works for you. What we choose to eat can enhance our lives
        while fighting the corporate greed that is making many of us and our
        planet sick. Here's some food for thought.
      </p>
      <p class="plus">+</p>
      <p>
        Support your local farmers by going to a farmers market in your area.
        Creating a bond with the people who cultivate & raise the food you eat
        is invaluable and you get the freshest food around.
      </p>
      <p>
        Make reading labels a habit when you approach a new product at the
        market. Look up anything you aren't yet familiar with. Avoid anything
        with a long ingredient list.
      </p>
      <p>
        Throw on some music and batch cook your staples for the week. Use your
        freezer and clean as you go. Cooking your own food is essential to
        eating well and saving money.
      </p>
      <p>
        Learning knife skills will save you energy, prevent you from being cut,
        and is the key to eating whole, unprocessed foods. Victorinox knives are
        an affordable, quality place to start.
      </p>
      <p>
        Our ancestors did not have food around all the time. We are built to
        fast occasionally to clean out our cells. Consume electrolytes when you
        fast such as pink salt or sugar-free electrolyte packets.
      </p>
      <p>
        Pausing before eating and having a moment of gratitude for the food you
        are consuming activates the rest & digest mode of the body aka the para-
        sympathetic nervous system. Tuning into this mode is vital to digest
        food properly and enhance our relationship to what we choose to consume.
      </p>
      <p class="minus">-</p>
      <p>
        Seed / “vegetable” oils are easily oxidized during manufacturing and
        when heated up aka cooking. When these oils oxidize they make trans-fats
        and lipid peroxides which leads to damage in DNA, proteins, and cell
        membrane lipids throughout the body. These oils are extracted with toxic
        solvents and are deodorized & bleached before being bottled. Deep fryer
        oil is often reused for multiple days so avoid fried food at all costs.
      </p>
      <p>
        Consuming foods and drinks with added sugar leads to inflammation,
        obesity, and diabetes. Our brains are wired to seek out sugar in the
        form of ripe fruit and store that sugar as fat in our bodies for times
        of scarcity. Food companies manipulate us through this innate wiring by
        adding sugar to their processed “foods”. Ideally sugary treats are
        homemade and include healthy fats - it'll also taste way better than
        anything you can buy.
      </p>
      <p>
        Phytates are found in grains, nuts, & legumes. Phytates bind to
        necessary minerals in the body which prevents absorption of those
        minerals. They can be reduced by fermenting, sprouting, & soaking these
        foods prior to consumption.
      </p>
      <p>
        Gluten is a protein found in certain grains that triggers inflammation
        because the body is not able to fully break it down which causes the
        release of zonulin which leads to a leaky gut. Other grains have similar
        but slightly less inflammatory proteins such as avenin in oats and zein
        in corn. Gluten enzymes are available for moments when one chooses to
        consume it.
      </p>
      <p>
        Do your best in any situation and don't worry when a situation doesn't
        align with your expectations. Stress is also a cause of inflammation.
        I've found digestive enzymes and activated charcoal helpful tools while
        traveling. Be where you are and enjoy the moment.
      </p>
