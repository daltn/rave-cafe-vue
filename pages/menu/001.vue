    <section id="braisedlamb" class="menu-card">
      <h2>Braised Lamb</h2>
      <p>by Dalton Saffe</p>
      <section class="menu-card-section">
        <b>Ingredients</b>
        <ul>
          <li>3-4ish lbs grass-fed lamb leg or shoulder</li>
          <li>1 large onion</li>
          <li>8 cloves garlic</li>
          <li>4 inch chunk ginger</li>
          <li>avocado oil</li>
          <li>
            whole spices: coriander, cumin, cloves, pepper, star anise, ceylon
            cinnamon, mustard seed, etc
          </li>
          <li>1 tsp chipotle powder</li>
          <li>3 Tbsp dried oregano</li>
          <li>fresh rosemary sprigs</li>
          <li>2 Tbsp coarse sea salt</li>
          <li>3 lemons - juiced</li>
          <li>1/4 cup apple cider vinegar</li>
          <li>2 cups filtered water</li>
        </ul>
      </section>
      <section class="menu-card-section">
        <b>Directions</b>
        <ul>
          <li>preheat oven to 300F / ~150C</li>
          <li>bring lamb to temperature and chop into 2-3 inch chunks</li>
          <li>toast whole spices in a dry skillet until aromatic and grind</li>
          <li>mix lamb with 1/2 spice mix, chi- potle, oregano & salt</li>
          <li>
            slice onions & chop garlic / ginger - heat up a large oven-safe pot
            on medium-high & add avocado oil
          </li>
          <li>sear the lamb in phases allowing space between the chunks</li>
          <li>
            set lamb aside & bring heat down - add rosemary & onions to pot
          </li>
          <li>stir and cook down for 10min</li>
          <li>add chopped garlic / ginger</li>
          <li>cook until fragrant ~ 2ish min</li>
          <li>
            add to pot: lamb, water, lemon, vinegar spice mix & whatever herbs
            you want to get creative w/ flavors
          </li>
          <li>mix and bring to a simmer</li>
          <li>cover & throw it in the oven</li>
          <li>leave in for 2.5 hours</li>
          <li>meat should be falling apart :)</li>
        </ul>
      </section>
    </section>

